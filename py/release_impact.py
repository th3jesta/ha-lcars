def _github_token() -> str:
    tok = os.environ.get("GITHUB_TOKEN", "")
    if not tok:
        try:
            tok = subprocess.check_output(["gh", "auth", "token"], text=True).strip()
        except Exception:
            pass
    if not tok:
        sys.exit("ERROR: No GitHub token found. Set GITHUB_TOKEN or authenticate via `gh auth login`.")
    return tok


def gh_get(path: str, token: str, params: dict | None = None) -> dict | list:
    url = f"https://api.github.com/{path.lstrip('/')}"
    headers = {
        "Authorization": f"Bearer {token}",
        "Accept": "application/vnd.github+json",
        "X-GitHub-Api-Version": "2022-11-28",
    }
    resp = requests.get(url, headers=headers, params=params, timeout=30)
    resp.raise_for_status()
    return resp.json()


def get_releases(token: str, per_page: int = 50) -> list[dict]:
    return gh_get(f"repos/{FRONTEND_REPO}/releases", token, params={"per_page": per_page})  # type: ignore[return-value]


def get_compare(token: str, base: str, head: str) -> dict:
    return gh_get(f"repos/{FRONTEND_REPO}/compare/{base}...{head}", token)  # type: ignore[return-value]


def get_release_by_tag(token: str, tag: str) -> dict:
    return gh_get(f"repos/{FRONTEND_REPO}/releases/tags/{tag}", token)  # type: ignore[return-value]


