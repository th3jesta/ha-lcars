def analyze_with_claude(
    release: dict,
    diff_text: str,
    lcars_context: str,
    pipeline_releases: list[dict] | None = None,
    base_tag: str | None = None,
    core_release: dict | None = None,
    pin_mode: str | None = None,
) -> str:
    """Call Claude Sonnet and return the raw markdown analysis.

    Three prompt framings:

    * `pipeline_releases` is set and `pin_mode is None` → the legacy pre-release
      pipeline framing (see PR #220): "one report covers this batch of beta
      frontend releases from the last stable frontend to the current head."
    * `pin_mode == "shipped"` → Core just pinned a new frontend on a released
      Core tag; the report covers `base_tag → release.tag_name` on the frontend
      repo, with intermediate frontend release notes attached. Filename lands on
      the Core tag that carried the pin.
    * `pin_mode == "pipeline"` → same, but for a Core pre-release; the base is
      whatever the last stable Core pinned. Used by --latest-beta.

    In both pin_mode framings, *core_release* is the Core release dict that
    bumped the pin, and *base_tag* is the previous frontend pin (before the
    bump).
    """
    import anthropic

    client = anthropic.Anthropic()

    tag = release["tag_name"]
    published = release["published_at"]
    body = release.get("body") or "(no release notes)"
    is_pre = release.get("prerelease", False)
    release_type = "pre-release/beta" if is_pre else "stable release"

    system = """\
You are an expert in Home Assistant frontend development and CSS theming. \
You analyze ha-frontend releases for breaking changes, variable renames, \
component refactors, and styling shifts that would affect a third-party \
Home Assistant theme.

The ha-lcars theme works by:
1. Defining CSS custom properties (variables) mapped to LCARS color palette entries
2. Using card-mod / UIX to inject CSS into Home Assistant's shadow DOM tree
3. Targeting specific HA web component element names, CSS classes, and CSS custom properties

Your job: given a release diff and the current ha-lcars theme source, produce a \
severity-organized Markdown report of changes that may require theme updates.

Severity levels:
- **HIGH**: Breaking — a renamed variable, removed element/class/slot, changed shadow DOM \
  structure, or new required layout will visually break the theme without a fix.
- **MEDIUM**: Degraded — something changed that affects appearance (color, spacing, sizing) \
  but may be tolerable or theme-neutral without an update.
- **LOW**: Informational — new features, new variables introduced, or component additions \
  that the theme could optionally style but doesn't need to right now.

Format rules:
- Output valid Markdown only (no code fences around the whole document).
- Start directly with the `## HIGH IMPACT` section (or a note that there are no high-impact items).
- Use `### Component: <name>` sub-headings within each severity section.
- For each item: what changed, what specific ha-lcars CSS rule or variable it affects, \
  and a concrete suggested fix (or "no fix needed" for LOW items).
- If the diff has no styling-relevant changes, say so explicitly in each section.
- Be precise: reference actual CSS property names, variable names, and element/class selectors \
  from both the diff and the LCARS theme source. Do not be vague.
"""

    if pin_mode is not None and core_release is not None:
        # Core just bumped its frontend pin. Frame as "these frontend versions
        # ship together in this Core release" so Claude reasons about the whole
        # range users are about to receive.
        core_tag = core_release["tag_name"]
        core_type = "pre-release" if core_release.get("prerelease") else "stable"
        span_label = "Upcoming Core release" if pin_mode == "pipeline" else "Core release"
        notes_blocks: list[str] = []
        for r in pipeline_releases or []:
            r_body = (r.get("body") or "_(no release notes)_").strip()
            r_type = "pre-release" if r.get("prerelease") else "stable"
            notes_blocks.append(
                f"### {r['tag_name']} — {r['published_at']} ({r_type})\n\n{r_body}"
            )
        notes_section = "\n\n".join(notes_blocks) if notes_blocks else \
            f"### {tag} — {published}\n\n{body}"
        header = (
            f"# {span_label}: Core `{core_tag}` ({core_type})\n"
            f"Frontend pin bumped: `{base_tag or '(none)'}`  →  `{tag}`\n"
            f"Frontend versions included: "
            f"{', '.join(r['tag_name'] for r in (pipeline_releases or [release]))}\n\n"
            f"## Release Notes (each ha-frontend release in this pin bump)\n"
        )
        body_for_prompt = f"{header}\n{notes_section}"
    elif pipeline_releases:
        # Legacy pre-release pipeline framing (no Core context supplied).
        notes_blocks = []
        for r in pipeline_releases:
            r_body = (r.get("body") or "_(no release notes)_").strip()
            notes_blocks.append(
                f"### {r['tag_name']} — {r['published_at']} "
                f"({'pre-release' if r.get('prerelease') else 'stable'})\n\n{r_body}"
            )
        notes_section = "\n\n".join(notes_blocks)
        header = (
            f"# ha-frontend Pre-Release Pipeline Analysis\n"
            f"Last stable: `{base_tag}`  →  Latest beta head: `{tag}`\n"
            f"Releases covered: {len(pipeline_releases)} "
            f"({sum(1 for r in pipeline_releases if r.get('prerelease'))} pre-release(s))\n\n"
            f"## Release Notes (chronological, all releases in window)\n"
        )
        body_for_prompt = f"{header}\n{notes_section}"
    else:
        body_for_prompt = (
            f"# ha-frontend Release: {tag} ({release_type})\n"
            f"Published: {published}\n\n"
            f"## Release Notes\n{body}"
        )

    user = f"""\
{body_for_prompt}

## Changed Files (diff)
{diff_text}

## ha-lcars Theme Source (current)
{lcars_context}

---

Analyze the changes above and produce the severity-organized impact report.
Focus on:
- CSS custom property renames or removals (HA uses `--ha-*`, `--mdc-*`, `--md-*`, etc.)
- Web component element name changes (`hui-*`, `ha-*` elements targeted by card-mod)
- Shadow DOM slot, part, or structure changes that would break `:host`, `::part()`, or slotted selectors
- New layout tokens (padding, sizing, border-radius vars) that the LCARS theme may need to adopt
- Changes to card background, color, or text-color application that would affect card-mod-injected rules

Do NOT report:
- Pure logic/behavior changes with no visual effect
- Translation/string changes
- Test file changes
- Changes to files not present in the diff above
"""

    try:
        message = client.messages.create(
            model="claude-sonnet-4-6",
            max_tokens=4096,
            system=system,
            messages=[{"role": "user", "content": user}],
        )
    except anthropic.BadRequestError as e:
        if "credit balance" in str(e):
            raise RuntimeError(
                "Anthropic API credits exhausted. Add credits at console.anthropic.com/settings/billing."
            ) from e
        raise

    return message.content[0].text  # type: ignore[index]


