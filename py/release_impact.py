def is_relevant(filename: str) -> bool:
    for suffix in SKIP_SUFFIXES:
        if suffix in filename:
            return False
    for prefix in RELEVANT_PATH_PREFIXES:
        if filename.startswith(prefix):
            return True
    # Also catch any file that explicitly touches CSS custom properties
    return False


def format_diff_for_prompt(compare: dict) -> tuple[str, list[str]]:
    """Return (formatted diff text, list of all changed filenames)."""
    all_files = compare.get("files", [])
    all_names = [f["filename"] for f in all_files]

    relevant = [f for f in all_files if is_relevant(f["filename"])]
    other = [f for f in all_files if not is_relevant(f["filename"])]

    lines: list[str] = []

    lines.append(f"Total files changed: {len(all_files)}")
    lines.append(f"Relevant to LCARS styling (included below): {len(relevant)}")
    lines.append(f"Other files (names only): {len(other)}")
    if other:
        lines.append("  " + ", ".join(f["filename"] for f in other[:40]))
        if len(other) > 40:
            lines.append(f"  ... and {len(other) - 40} more")
    lines.append("")

    included = relevant[:MAX_DIFF_FILES]
    for f in included:
        lines.append(f"--- {f['filename']}  (+{f['additions']} -{f['deletions']}) ---")
        patch = f.get("patch", "")
        if patch:
            if len(patch) > MAX_PATCH_CHARS:
                patch = patch[:MAX_PATCH_CHARS] + f"\n... [truncated, {len(patch) - MAX_PATCH_CHARS} more chars]"
            lines.append(patch)
        else:
            lines.append("[binary or empty diff]")
        lines.append("")

    return "\n".join(lines), all_names


